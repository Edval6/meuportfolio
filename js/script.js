var im1 = document.querySelectorAll(".imagem")[0];

im1.onmouseover = function(){
    var titulo = document.querySelectorAll(".foto-texto")[0];
    titulo.style.display = "block";
}

im1.onmouseout = function(){
    var titulo = document.querySelectorAll(".foto-texto")[0];
    titulo.style.display = "none";
}

